package praticeSet2;

public class ExpressionResult {
	public static void main(String[] args) {
		//java will do this method in int format
		float a=7/4 * 9/2;
		System.out.println("Expression result = " +a);
		
		//we we want float value we have to put a float in the program
		float b=7/4.0f * 9/2.0f;
		System.out.println("Expression result = " +b);
	}

}
